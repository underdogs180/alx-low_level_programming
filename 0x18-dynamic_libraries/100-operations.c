#include <stdio.h>
#include "main.h"

/**
 *  * add - adds two integer
 *   * @a: int type number
 *    * @b: int type number
 *     * Return: sucessfully
 *      */
int add(int a, int b)
{
		return (a + b);
}

/**
 *  * sub - subtract two integers
 *   * @a: int type number
 *    * @b: int type number
 *     * Return: sucessfully
 *      */

int sub(int a, int b)
{
		return (a - b);
}

/**
 *  * mul - multiply two integers
 *   * @a: int type number
 *    * @b: int type number
 *     * Return: sucessfully
 *      */
int mul(int a, int b)
{
		return (a * b);
}

/**
 *  * div - divide two integers
 *   * @a: int type number
 *    * @b: int type number
 *     * Return: sucessfully
 *      */
int div(int a, int b)
{
		return (a / b);
}

/**
 *  * mod - divides  two integers
 *   * @a: int type number
 *    * @b: int type number
 *     * Return: sucessfully
 *      */
int mod(int a, int b)
{
		return (a % b);
}
